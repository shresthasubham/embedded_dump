		MOV P0, A
		ACALL DELAY
		ACALL SW
		MOV P0, A
		ACALL DELAY
		SJMP START

SW:		RR A
		RR A
		RR A
		RR A
		
DELAY: 	MOV R7, #255
HERE1: 	MOV R6, #255
HERE2: 	MOV R5, #255
HERE3: 	DJNZ R5, HERE3
		DJNZ R6, HERE3
		DJNZ R7, HERE3
		RET
		
		END
		